/* Write a program to convert Celsius (Centigrade degrees temperature to
Fahrenheit)*/

#include<stdio.h>

int main(){
     int T;
     
     printf("Welcome to CELCIUS to FAHRENHEIT converter\n\n");
     
     printf("ENTER TEMPERATURE\n");   
     scanf("%d", &T);
     
     printf("The fahrenheit value of your temperature is %f\n",T*1.8+32);
     
     printf("CODED BY THE LEGEND\n  GIRIRAJ");
    return 0;
}
