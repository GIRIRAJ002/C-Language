#include<stdio.h>

int main(){
    int g=1;
    
    float d;
    d=2.7;
    
    char c;
    c= 'g';
    
    printf("the value of g is %d\n",g);
    printf("the value of d is %f\n",d);
    printf("the value of c is %c\n", c);
    
    return 0;
}
/* data are of three types
1.integer
2.real  numbers e.g 2.7 
3. characters e.g a b c  
NOTE-
>%d for integers 
>%f for real values (floating point numbers)
>%c for characters
\n is used to execute functons in different lines
*/